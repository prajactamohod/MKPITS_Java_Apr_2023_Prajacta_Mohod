class DivideArgumentwithreturntype{
//Argumentwithreturntype-------------
public int Divide (int number1,int number2){
return number1+number2;
}
public static void main(String[]args){
DivideArgumentwithreturntype cal=new DivideArgumentwithreturntype();
int div=cal.Divide(80,2);
System.out.println("sum of two number="+div);
}
}