class SubstractArgumentwithreturntype{
//Argumentwithreturntype-------------
public int Substraction(int number1,int number2){
return number1+number2;
}
public static void main(String[]args){
SubstractArgumentwithreturntype cal=new SubstractArgumentwithreturntype();
int sub=cal.Substraction(40,22);
System.out.println("sum of two number="+sub);
}
}