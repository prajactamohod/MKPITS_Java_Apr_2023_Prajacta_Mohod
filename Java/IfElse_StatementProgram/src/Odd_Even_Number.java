public class Odd_Even_Number {

        public static void main(String[] args) {

            int number=12;                  //defining a variable(Assing the number)

            if(number%2==0){                 //(Condition)  Check if the number is divisible by 2 or not

                System.out.println("even number");

            }

            else{
                System.out.println("odd number");
            }
        }
    }

