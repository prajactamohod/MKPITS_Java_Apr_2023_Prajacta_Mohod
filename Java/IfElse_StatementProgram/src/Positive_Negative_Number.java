public class Positive_Negative_Number {


    public static void main(String[] args) {
        int count = 10;


        if (count > 0) {    // // checks if number is greater than 0

            System.out.println("The number is positive.");    // execute this block
        }



        else {
            System.out.println("The number is not positive.");  //// if number is not greater than 0
        }

        System.out.println("Statement outside if...else block");
    }
}
